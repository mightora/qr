/* ============================================================
   export.js — Export Module
   ============================================================ */

const QRExport = (() => {

  const PAYLOAD_LABEL_MAX_CHARS = 60; // max chars shown as label in PDF export

  /**
   * Export QR code as SVG file.
   * qrcodejs renders a canvas; we wrap it as an SVG image.
   * @param {HTMLElement} qrContainer
   * @param {string} filename
   */
  function exportSVG(qrContainer, filename = 'qr-code.svg') {
    const canvas = _getCanvas(qrContainer);
    if (!canvas) { _notifyNoQR(); return; }

    const width = canvas.width;
    const height = canvas.height;
    const dataURL = canvas.toDataURL('image/png');

    const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink"
     width="${width}" height="${height}" viewBox="0 0 ${width} ${height}">
  <image href="${dataURL}" width="${width}" height="${height}"/>
</svg>`;

    _download(new Blob([svg], { type: 'image/svg+xml' }), filename);
    UI.showToast('SVG exported successfully!', 'success');
  }

  /**
   * Export QR code as PNG at given scale.
   * @param {HTMLElement} qrContainer
   * @param {string} filename
   * @param {number} scale - 1, 2, or 4
   */
  function exportPNG(qrContainer, filename = 'qr-code.png', scale = 2) {
    const canvas = _getCanvas(qrContainer);
    if (!canvas) { _notifyNoQR(); return; }

    if (scale === 1) {
      canvas.toBlob(blob => { _download(blob, filename); UI.showToast('PNG exported!', 'success'); }, 'image/png');
      return;
    }

    const scaled = document.createElement('canvas');
    scaled.width = canvas.width * scale;
    scaled.height = canvas.height * scale;
    const ctx = scaled.getContext('2d');
    ctx.imageSmoothingEnabled = false;
    ctx.drawImage(canvas, 0, 0, scaled.width, scaled.height);
    scaled.toBlob(blob => { _download(blob, filename); UI.showToast(`PNG (${scale}×) exported!`, 'success'); }, 'image/png');
  }

  /**
   * Export QR code as PDF.
   * @param {HTMLElement} qrContainer
   * @param {string} filename
   * @param {boolean} includeLabel - include payload label below QR
   */
  function exportPDF(qrContainer, filename = 'qr-code.pdf', includeLabel = true) {
    const canvas = _getCanvas(qrContainer);
    if (!canvas) { _notifyNoQR(); return; }

    try {
      const { jsPDF } = window.jspdf;
      const imgData = canvas.toDataURL('image/png');
      const qrSize = 100; // mm
      const margin = 20;
      const pageW = qrSize + margin * 2;
      const pageH = includeLabel ? qrSize + margin * 2 + 20 : qrSize + margin * 2;

      const pdf = new jsPDF({ orientation: 'portrait', unit: 'mm', format: [pageW, pageH] });
      pdf.addImage(imgData, 'PNG', margin, margin, qrSize, qrSize);

      if (includeLabel && window.AppState && window.AppState.currentPayload) {
        pdf.setFontSize(7);
        pdf.setTextColor(100, 100, 100);
        const truncated = window.AppState.currentPayload.substring(0, PAYLOAD_LABEL_MAX_CHARS);
        pdf.text(truncated + (window.AppState.currentPayload.length > PAYLOAD_LABEL_MAX_CHARS ? '...' : ''), pageW / 2, margin + qrSize + 10, { align: 'center' });
      }

      pdf.setFontSize(8);
      pdf.setTextColor(150, 150, 150);
      pdf.text('Generated by QR Studio — qr.mightora.io', pageW / 2, pageH - 8, { align: 'center' });
      pdf.save(filename);
      UI.showToast('PDF exported!', 'success');
    } catch (err) {
      console.error('PDF export failed:', err);
      UI.showToast('PDF export failed. Please try PNG instead.', 'error');
    }
  }

  /**
   * Export multiple QR containers as a ZIP of PNGs.
   * @param {Array<HTMLElement>} qrContainers
   * @param {string} filename
   */
  async function exportZIP(qrContainers, filename = 'qr-codes.zip') {
    if (!qrContainers || !qrContainers.length) { _notifyNoQR(); return; }

    try {
      const zip = new JSZip();
      const folder = zip.folder('qr-codes');

      for (let i = 0; i < qrContainers.length; i++) {
        const canvas = _getCanvas(qrContainers[i]);
        if (!canvas) continue;

        const blob = await new Promise(res => canvas.toBlob(res, 'image/png'));
        folder.file(`qr-part-${i + 1}.png`, blob);
      }

      const zipBlob = await zip.generateAsync({ type: 'blob' });
      _download(zipBlob, filename);
      UI.showToast('ZIP downloaded!', 'success');
    } catch (err) {
      console.error('ZIP export failed:', err);
      UI.showToast('ZIP export failed.', 'error');
    }
  }

  /**
   * Export a single QR container as ZIP (all formats).
   */
  async function exportSingleAsZIP(qrContainer, baseName = 'qr-code') {
    const canvas = _getCanvas(qrContainer);
    if (!canvas) { _notifyNoQR(); return; }

    try {
      const zip = new JSZip();
      const pngBlob = await new Promise(res => canvas.toBlob(res, 'image/png'));
      zip.file(`${baseName}.png`, pngBlob);

      // Add 2x PNG
      const scaled = document.createElement('canvas');
      scaled.width = canvas.width * 2;
      scaled.height = canvas.height * 2;
      const ctx = scaled.getContext('2d');
      ctx.imageSmoothingEnabled = false;
      ctx.drawImage(canvas, 0, 0, scaled.width, scaled.height);
      const png2x = await new Promise(res => scaled.toBlob(res, 'image/png'));
      zip.file(`${baseName}@2x.png`, png2x);

      // Add SVG
      const w = canvas.width, h = canvas.height;
      const dataURL = canvas.toDataURL('image/png');
      const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" width="${w}" height="${h}" viewBox="0 0 ${w} ${h}">
  <image href="${dataURL}" width="${w}" height="${h}"/>
</svg>`;
      zip.file(`${baseName}.svg`, svg);

      const zipBlob = await zip.generateAsync({ type: 'blob' });
      _download(zipBlob, `${baseName}.zip`);
      UI.showToast('ZIP package downloaded!', 'success');
    } catch (err) {
      console.error('ZIP export failed:', err);
      UI.showToast('ZIP export failed.', 'error');
    }
  }

  /* ---- helpers ---- */

  function _getCanvas(container) {
    if (!container) return null;
    return container.querySelector('canvas');
  }

  function _download(blob, filename) {
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    setTimeout(() => URL.revokeObjectURL(url), 5000);
  }

  function _notifyNoQR() {
    if (window.UI) UI.showToast('Generate a QR code first!', 'warning');
    else alert('Generate a QR code first!');
  }

  return { exportSVG, exportPNG, exportPDF, exportZIP, exportSingleAsZIP };

})();

window.QRExport = QRExport;
